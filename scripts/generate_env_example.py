def _env_name(field_name: str) -> str:
    return field_name.upper()


def _default_repr(field_info: FieldInfo) -> str:
    default = field_info.default
    if default is None:
        return ""
    # SecretStr default: extract inner value
    try:
        from pydantic import SecretStr

        if isinstance(default, SecretStr):
            return default.get_secret_value()
    except ImportError:
        pass
    if isinstance(default, bool):
        return "true" if default else "false"
    if isinstance(default, int):
        return str(default) if default != 0 else ""
    if isinstance(default, str):
        return default
    return ""


def generate(output_path: Path | None = None) -> str:
    """Generate .env.example content and optionally write to disk."""
    model_fields: dict[str, FieldInfo] = Settings.model_fields

    # bucket fields into categories
    buckets: dict[str, list[str]] = {cat_id: [] for cat_id, _ in _CATEGORIES}

    for field_name in model_fields:
        cat = _FIELD_CATEGORY.get(field_name, "misc")
        buckets.setdefault(cat, [])
        buckets[cat].append(field_name)

    # sort within each category for determinism
    for cat_id in buckets:
        buckets[cat_id].sort()

    lines: list[str] = [
        "# .env.example — omnimarket",
        "#",
        "# Generated by scripts/generate_env_example.py from the Settings model.",
        "# Do NOT hand-edit. Re-generate after adding or removing Settings fields:",
        "#",
        "#   uv run python scripts/generate_env_example.py",
        "#",
        "# All values are empty / false / 0 by default.",
        "# Enable a service by setting its ENABLE_* flag to true and filling",
        "# in the required fields documented below.",
        "",
    ]

    for cat_id, cat_label in _CATEGORIES:
        fields_in_cat = buckets.get(cat_id, [])
        if not fields_in_cat:
            continue

        lines.append(f"# {'=' * 70}")
        lines.append(f"# {cat_label}")
        lines.append(f"# {'=' * 70}")
        lines.append("")

        for field_name in fields_in_cat:
            field_info = model_fields[field_name]
            description = field_info.description or ""
            env_key = _env_name(field_name)
            default_val = _default_repr(field_info)

            # wrap description at ~78 chars
            desc_words = description.split()
            desc_lines: list[str] = []
            current = ""
            for word in desc_words:
                probe = f"{current} {word}".strip()
                if len(probe) > 76:
                    if current:
                        desc_lines.append(current)
                    current = word
                else:
                    current = probe
            if current:
                desc_lines.append(current)

            for dl in desc_lines:
                lines.append(f"# {dl}")
            lines.append(f"# {env_key}={default_val}")
            lines.append("")

    # Strip trailing blank lines so the file ends with exactly one newline.
    while lines and lines[-1] == "":
        lines.pop()
    content = "\n".join(lines) + "\n"

    if output_path is not None:
        output_path.write_text(content, encoding="utf-8")
        print(f"Written: {output_path}")

    return content


