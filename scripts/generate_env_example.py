"""Generate .env.example from the omnimarket Settings model.

Introspects Settings field metadata to produce a commented, categorised
.env.example. Output is deterministic — sorted within each category, no
timestamps, no machine-specific literals. Run from the repo root:

    uv run python scripts/generate_env_example.py

OMN-10581. Wave 1 / Task 9 of the Public-Shippable plan.
"""

